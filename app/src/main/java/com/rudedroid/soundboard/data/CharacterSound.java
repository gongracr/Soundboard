package com.rudedroid.soundboard.data;

public class CharacterSound {

  private String soundTitle;
  private String soundDriveFileId;
  private String assetName;

  public String getSoundTitle() {
    return soundTitle;
  }

  public void setSoundTitle(String soundTitle) {
    this.soundTitle = soundTitle;
  }

  public String getSoundDriveFileId() {
    return soundDriveFileId;
  }

  public void setSoundDriveFileId(String soundDriveFileId) {
    this.soundDriveFileId = soundDriveFileId;
  }

  public String getAssetName() {
    return assetName;
  }

  public void setAssetName(String assetName) {
    this.assetName = assetName;
  }
}
